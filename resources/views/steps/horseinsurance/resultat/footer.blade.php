<script type="text/javascript">

    function docFilter() {

        var checked = $('input[name="veterinarvardsforsakring"]:checked').val();
        var checked2 = $('input[name="livforsakring"]:checked').val();

        $('.info-columns .col').removeClass('include');
        $('.info-columns .col-vet[data-attributes="' + checked + '"]').addClass('include');
        $('.info-columns .col-liv[data-attributes="' + checked2 + '"]').addClass('include');

    }

    $(document).ready(function () {

        $('.accordion-tabs .tab-title').on('click', function () {

            var current = $(this).next('.tab-content');

            $('.accordion-tabs li').removeClass('open');
            $('.accordion-tabs .tab-content').slideUp(300);

            if (!$(current).is(':visible')) {
                $(current).slideDown(300);
                $(this).parent().addClass('open');
            }
        });
    });

    $('input[name="veterinarvardsforsakring"]').on('change', function () {
        setTimeout(function () {
            docFilter();
        }, 500)
    });

    $('.frame-resultat-template').on('change', 'input[name="livforsakring"]', function () {
        docFilter();
    });

</script>
